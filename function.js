// function

// define
function add(a,b){
    return a+b;
}

console.log(add(5,2));  // calling


// arrow function
const addArrow = (a,b) => {
    return a+b;
}

console.log(addArrow(5,4));  // calling

// arrow function for multiplication of two numbers
const mul = (a,b) => {
    return a*b;
}
console.log(mul(2,8));